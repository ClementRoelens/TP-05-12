package org.example.Classes;

public class MagicalSkill {
    String magicalSkill;
    public MagicalSkill(String magicalSkill) {
        this.magicalSkill = magicalSkill;
    }

    @Override
    public String toString() {
        return "MagicalSkill{" +
                "magicalSkill=" + magicalSkill +
                '}';
    }
}